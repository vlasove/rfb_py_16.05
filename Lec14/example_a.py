def add(a, b):
    return a**2 + b**3

print(add(10, 20))
print(add(20, 10))

# print(add(1)) # Нельзя указывать меньше аргументов, чем в сигнатуре
print(add(1, 2))
# print(add(1,2,3)) # Нельзя указывать больше аругментов, чем ...

# Явная передача аргументов через имена входных парамтеров
