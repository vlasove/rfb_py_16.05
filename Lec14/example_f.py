def add(a :int, b :int) -> int:
    """
    description.........
    """
    return a + b

def main() -> None:
    """
    entry point
    """
    print(add(2,3))
    print(add("a", "b"))
    print(add([10,20,30], [40]))
    print(add((2,3), (4,5)))
    print(add(10.5, 10 ** 0.5))

main()